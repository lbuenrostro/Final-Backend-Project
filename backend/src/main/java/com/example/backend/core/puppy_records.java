package com.example.backend.core;

public class puppy_records {
    public String breed;
    public String gender;
    public String age;
    public Integer price;

    public puppy_records() {}

    public puppy_records(String breed, String gender, String age, Integer price) {
        this.breed = breed;
        this.gender = gender;
        this.age = age;
        this.price = price;
    }
}
